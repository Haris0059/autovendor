export function listingsForAccount(accountId: number): OlxListing[] {
  return mockListings.filter((l) => l.account_id === accountId);
}

export function listingsByStatus(
  accountId: number,
  status?: string
): OlxListing[] {
  const list = listingsForAccount(accountId);
  if (!status) return list;
  return list.filter((l) => l.status === status);
}
